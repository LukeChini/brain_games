<span>Redefina a sua senha formulário de Redefinição de Senha. (Certifique-se de usar o endereço de e-mail vinculado a sua conta na Brain Games). Se você não receber o e-mail de redefinição de senha, verifique a sua pasta de spam. Se ele não estiver lá, envie um e-mail para a nossa equipe de atendimento ao cliente, e redefiniremos a senha para você.</span>
</li>
<li>
<span>Se você ainda está tendo problemas, exclua qualquer senha armazenada automaticamente. Isso vai garantir que informações inválidas das senhas não sejam inseridas automaticamente pelo seu computador ou navegador.</span>
</li>
</ol>
