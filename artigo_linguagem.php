                <p align="justify">
                Você já ouviu falar sobre a última pesquisa do cérebro? Se você estimular o seu cérebro com desafios cognitivos, aumentará os seus níveis de dopamina. A dopamina é um neurotransmissor produzido pelo cérebro para facilitar a passagem de sinais entre as células cerebrais. A dopamina também o faz sentir-se melhor, causando um sentimento de recompensa, de satisfação, especialmente quando o cérebro trabalha em uma tarefa com o hemisfério esquerdo, como, por exemplo, uma tarefa de palavras. O mesmo sistema de recompensa por estímulo é ativado por alguns narcóticos. (Isso nos torna “traficantes”?) O seu cérebro adora sentir-se bem. Se ele não puder fazer isso, solucionando enigmas, buscará outros meios. Geralmente, os enigmas são melhores para você do que outros meios, que não acrescentam muito ao seu vocabulário.
                </p>
                <p align="justify">
                Vamos imaginar, apenas por um momento, quanta sorte nós humanos temos por sermos capazes de aprender o nosso idioma sozinhos, tão pequenos, que pensamos que a vida deve dar muito trabalho. Os bebês e as crianças pequenas aprendem a estrutura básica, a pronúncia, a gramática e a maior parte do vocabulário de seus idiomas nativos, independentemente das habilidades de ensinar de seus pais. A autoeducação na aquisição de uma habilidade assim complexa é uma pitada de brilhantismo no design do cérebro.
                </p>
                <p align="justify">
                É uma segunda manobra de mestre convocar um neurotransmissor automotivador, como a dopamina, como um participante naquele processo. Em conjunto, eles parecem verificar o design do cérebro e como o design da última Constituição política, requer uma avaliação real das motivações humanas e autointeresse.
                </p>
                <p align="justify">
                O inglês é mais complicado do que a maioria dos idiomas. É mais difícil ler, escrever, falar palavras em inglês e entender uma conversa rápida e parágrafos impressos. Por exemplo, a incidência de dislexia, é menor nas comunidades que não são de língua inglesa. A diferença não é a escolaridade, é a consistência da linguagem. Os 40 sons estranhos em inglês podem ser escritos em mais de 1.000 diferentes maneiras. O cérebro tem que trabalhar mais depressa para decodificar as frases em inglês (“Aquele “gh” é “ff” em tough ou “gas” em ghastly, ou é outra coisa?”) Em italiano, por exemplo, você fala exatamente o que vê, sempre. O fato de que a Itália tem a metade de crianças disléxicas per capita em relação aos EUA , pode ser pelo fato de que o cérebro humano pode decodificar os sons de uma frase em italiano milissegundos mais depressa do que a mesma frase em inglês. São necessários milissegundos para processar na pré-escola poderia lhe dizer isso (assim que ela aprender a falar seu idioma nativo, é claro).
                </p>
                <p align="justify">
