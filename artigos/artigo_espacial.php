                <p align="justify">
Bloco de notas visual-espacial é o termo que os neurocientistas usam para se referir a uma ferramenta da Memória Operacional que você usará ao realizar tarefas, tais como o exercícios mental nas seguintes páginas. Alguns dos exercícios exigem que você visualize as formas no espaço, como fazem os arquitetos, construtores, escultores e mestres de xadrez. Por exemplo, na vida diária, as pessoas usam essa habilidade para colocar malas no porta-malas do carro, encontrar o caminho para a entrada de um prédio ou localizar seus carros no estacionamento de um shopping.
</p>
<p align="justify">
O hemisfério direito da maioria dos cérebros masculinos é mais especializado neste tipo de habilidade do que é geralmente o cérebro feminino. Essa é um das diferenças mais óbvias do sexo no processamento cognitivo e parece relacionar-se com o que é conhecido sobre como os primatas proto-humanos viveram, quando seus cérebros estavam evoluindo, 60 milhões de anos atrás. As mulheres encontram o caminho identificando pontos de referência. Muito provavelmente, os homens se orientam em grandes pontos de referência geográficos, incluindo o Sol e as estrelas. É tentador especular que, como os caçadores, os machos viajavam longas distâncias atraídos pela caça em um território  desconhecido. As mulheres, que ficavam perto de suas habitações para cuidar dos filhos e fugir dos predadores, muito provavelmente teria baseando-se em pontos de referência locais para se movimentar em busca de alimento ou madeira para acender o fogo em um território familiar, perto de suas moradias.
</p>
<p align="justify">
As habilidades visual-espaciais podem ser desenvolvidas com a prática. Um estudo neurocientífico recente sobre motoristas de táxi profissionais e experientes de Londres, que precisavam passar por rigorosos testes para descobrir endereços em qualquer lugar na cidade antes de receberem a licença, mostrou que uma parte do cérebro deles era significativamente maior do que a dos trabalhadores de outros serviços em Londres. Chama-se hipocampo, com base na palavra latina para cavalo (como também é hipopótamo), pois em um momento, alguém deve ter pensado que o órgão e o animal se pareciam com um cavalo (o que não é verdade). Cada cérebro tem um de cada lado. O do lado direito, onde estão localizadas as habilidades visual-espaciais na maioria das pessoas, era maior nos cérebros dos taxistas londrinos. Os neurônios na parte de trás daquela área enviavam mais conexões para responder ao desempenho diário exigido deles. Isso forçou um aumento na massa, sendo claramente perceptível em uma tomografia cerebral.
</p>
<p align="justify">
Tal descoberta é um exemplo convincente de um fato que é pouco conhecido, apesar de muito encorajador. O cérebro humana é capaz de se adaptar fisicamente para atingir as exigência impostas, muito parecido como uma sociedade pode mudar seus valores em uma crise comum ou uma flor crescerá na direção do Sol. Isso significa que o cérebro responde como quaisquer outros sistemas físicos. O exercício aeróbico dá força ao músculo do coração e capacidade de transportar oxigênio aos pulmões, aumentando, dessa forma, a resistência. Grupos de músculos ganham massa quando são sujeitos a estresses regulares de exercícios direcionados. Como resultado, aumenta a capacidade de levantar e correr. Ainda que um sistema não tenha sido usado por muitos anos, a sua massa e eficiência podem ser revividas com exercício e como consequência, aparecendo drasticamente nas pessoas mais velhas que começam a se exercitar depois de passar suas vidas relativamente sedentárias.
</p>
<p align="justify">
Como a vida, existe o outro ladro. Na Idade Média, as artes de arquitetura, construção e habilidade para o desenho – todas as profissões que visam os centros visual-espaciais no hemisfério direito – eram conhecidas como artes de melancolia, pois elas supostamente causavam depressão no espirito. Na verdade, o hemisfério direito processa expressões faciais negativas e é mais ativo na fase depressiva de um ciclo maníaco-depressivo. No entanto, isso não deteve Leonardo da Vinci, nem deveria deter você.

