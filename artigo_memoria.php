              <article class="col-12">
                <p  align="justify">
Isto trata da(s) memória(s), no plural, porque o cérebro usa muitas estratégias diferentes para armazenar dados, recuperá-los e distorcê-los. Se você conhecer alguma coisa sobre como esses sistemas funcionam, poderá usar as estratégias dos sistemas internos da memória do cérebro para conceber a melhor estratégia a cada vez.
<p align="justify">
A <dfn>memória explícita</dfn> (às vezes chamada de <dfn>memória declarativa<dfn>) é o tipo que você usa quando faz, conscientemente, um esforço para se lembrar de alguma coisa. O seu cérebro armazena lembranças implícitas, não declarativas, sem que você sequer saiba o que aconteceu, muito menos sem ter a ciência de tentar. Por exemplo, é como você aprendeu o seu idioma nativa.
</p>

<p align="justify">
Aqui estão duas estratégias para converter novos dados em memória explícita de longa duração. Repita os dados de diferentes formas: escreva-os, diga-os em voz alta, explique-os a outra pessoa, faça um diagrama deles. Deixe que repousem um pouco, depois, recomece. Espaçar tais sessões de repetição te ajudará.
</p>

<p align="justify" align="justify">
Outro método de aprender novos dados é pegar um truque do seu sistema de <dfn>memória implícita</dfn>. A sua memória implícita de um acontecimento (chamada de <dfn>memória episódica</dfn>) torna-se permanente porque ela emocionalmente alterada – o dia do seu casamento, um acidente ou o nome do seu primeiro amor, por exemplo. Frequentemente, você pode aplicar artificialmente um “rótulo” emocional nos fatos banais, associando-os a alguma outra lembrança estranha ou dramática.
</p>
<p align="justify">

Relacione todos os novos dados às lembranças existentes. Quanto mais familiares forem os laços com os quais você lida com um novo fato, mais provavelmente será capaz de lembrar. Por quê? A sua lembrança de alguma coisa que aconteceu a você está armazenada em muitas partes diferentes do seu cérebro. Os cheiros dela em um lugar, as cores, as sensações de toque, os sons em diferentes conjuntos de células. Quando você se lembra, qualquer um desses componentes pode acionar o cérebro para que ele se mova e colete todos os outros, para reconstruir a riqueza de todo o acontecimento. Quando você tenta aceitar uma descrição de um “acontecimento” na memória, diga uma data na história ou uma lista de critérios para um diagnóstico, crie tantos “acionadores” quanto puder visualizando o “acontecimento” em associação com muitos sentimentos diferentes a lugares familiares, ou outros eventos.
</p>
<p align="justify">

As estratégias são importantes se você deseja aprimorar as habilidades da memória porque a prática não ajuda. Onde a prática ajuda é ao prolongar o tempo que você pode concentrar-se em uma tarefa, muito parecido como você desenvolve a capacidade pulmonar, então a resistência, com exercício aeróbico. A curta respostas para desenvolver a habilidade de focar mais é que forçar a si mesmo a permanecer com uma tarefa. Portanto, não afaste o enigma na primeira vez em que encontrar um obstáculo ao trabalhar com ele. Faça uma pausa, se quiser, pense por um minuto, mas não desista. Se você ficar parado, olhe a dica impressa de cabeça para baixo para continuar em frente. Cada vez que você desiste, desenvolve a capacidade de permanecer com ele mais tempo, ainda que seja pouco. O efeito é cumulativo e vale o esforço. Se você não conseguir concentrar-se, não poderá desenvolver as estratégias da memória explícita e precisará fornecer novos dados à memória.
</p>
<p align="justify">

Fique atento à falsa lembrança. A verdadeira pode ficar facilmente distorcida quando você se lembra dela. O cérebro organiza o amplo armazenamento de dados que ele deve lembrar, juntando seus pedaços de modo que acompanhem outra experiência passada. Quando faltam detalhes, ele tenta inserir o que “deve” ter acontecido. Ele também preencherá os detalhes que são sugeridos pela forma como uma pergunta é feira quando você é solicitado a lembrar de alguma coisa.
</p>
<p align="justify">
O motivo  pelo qual você esquece releva alguma coisa sobre porque se lembra. Quando duas partes de dados chamam a sua atenção em sequência, como, por exemplo, quando você lê duas estórias em uma antologia, a sua lembrança de detalhes da primeira ficará obscurecida por um tempo, uma ocorrência natural chamada de inibição retroativa. É por isso que, às vezes, as pessoas se veem em um lugar, mas não conseguem lembrar o motivo de terem ido para lá. Normalmente, é porque alguma coisa interessante chamou a atenção no caminho para lá.
</p>

