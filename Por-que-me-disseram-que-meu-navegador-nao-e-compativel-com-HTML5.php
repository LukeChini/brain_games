      <h1> Por que me disseram que meu navegador não é compatível com HTML5?</h1>
    </header>

    <div class="article-body">
      <p>Alguns dos nossos jogos de matemática usam HTML5 (um método alternativo para exibir vídeos e gráficos online, que não precisa do Adobe Flash Player). Esses jogos têm requisitos diferentes dos jogos em Flash. Se você estiver usando um navegador incompatível, você verá a mensagem "Para jogar este jogo, é necessário usar um navegador compatível com HTML5".</p>
<p><span class="wysiwyg-font-size-large"><strong>Quais navegadores são compatíveis com jogos em HTML5?</strong></span></p>
<p><span class="wysiwyg-font-size-medium"><a href="http://www.google.com/chrome" target="_blank" rel="noopener noreferrer"><strong><img class="no-border" src="https://help.lumosity.com/hc/en-us/article_attachments/209995023/chrome_logo.jpg" alt="">&nbsp;</strong>Chrome</a></span></p>
<p><span class="wysiwyg-font-size-medium"><a href="http://getfirefox.com" target="_blank" rel="noopener noreferrer"><strong><img class="no-border" src="https://help.lumosity.com/hc/en-us/article_attachments/209995063/firefox_logo.png" alt="">&nbsp;</strong>Firefox</a></span></p>
<p><span class="wysiwyg-font-size-medium"><img class="no-border" src="https://help.lumosity.com/hc/en-us/article_attachments/210103326/edge_logo.png" alt="">&nbsp;&nbsp;<a href="https://www.microsoft.com/windows/microsoft-edge" target="_blank" rel="noopener noreferrer">Edge&nbsp;</a></span>(instalado de fábrica no Windows 10 apenas)</p>
<p><strong>Verifique se o navegador está atualizado.</strong></p>
<p><a href="http://supportdetails.com" target="_blank" rel="noopener noreferrer">Encontre a versão de seu navegador e sistema operacional.</a></p>
<p><span class="wysiwyg-font-size-large"><strong>Problemas com navegadores compatíveis?</strong></span></p>
<p>Os jogos em HTML5 requerem certos drivers e placas gráficas (o hardware usado para executar gráficos de vídeo). Verifique se há atualizações disponíveis no seu computador. Se ainda tiver problemas, sua placa gráfica pode não ser compatível com HTML5.</p>
